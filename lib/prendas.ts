export function obtenerPrendasPorCategoria(categoria: string): Prenda[] {
  if (!categoria) return prendas;
  return prendas.filter(p => p.categoria === categoria);
}

export function buscarPrendas(termino: string): Prenda[] {
  if (!termino) return prendas;
  const terminoLower = termino.toLowerCase();
  return prendas.filter(p =>
    p.nombre.toLowerCase().includes(terminoLower) ||
    p.descripcion.toLowerCase().includes(terminoLower) ||
    p.categoria.toLowerCase().includes(terminoLower) ||
    p.tags.some(t => t.toLowerCase().includes(terminoLower))
  );
}

export function obtenerCategorias(): string[] {
  return Array.from(new Set(prendas.map(p => p.categoria))).sort();
}

export function obtenerPrendaPorId(id: number): Prenda | undefined {
  return prendas.find(p => p.id === id);
}

